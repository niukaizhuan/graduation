package com.example.demo.response;

import lombok.Data;

/**
 * 统一返回值
 * 
 * @author: 王越
 * @date: 2019-06-03 01:11:32
 */
@Data
public class CommonResp {

	/**
	 * 状态码
	 */
	private Integer code;

	/**
	 * 返回信息
	 */
	private String msg;

	/**
	 * 返回参数对象
	 */
	private Object obj;

	public CommonResp(Integer code, String msg, Object obj) {
		super();
		this.code = code;
		this.msg = msg;
		this.obj = obj;
	}

	public CommonResp() {
		super();
	}

}
